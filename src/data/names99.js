// Helper: expand a compact row of [id, name, meaning] triples into full name objects.
function bouquetRow(bouquet, orderStart, entries) {
  return entries.map(([id, name, meaning], i) => ({
    id, name, bouquet, order: orderStart + i,
    meaning,
    thanaa: 'سبحان ' + name.replace(/^(ال|ﭐﻟ)/, 'ال'),
    talab:  makeTalab(name),
  }))
}

function makeTalab(name) {
  const stripped = name.replace(/^(ال|ﭐﻟ)/, '')
  return `يا ${stripped}، أَجرِ عليَّ من فضلك، وارزقني من بركة هذا الاسم.`
}

// Index by bouquet for the chart layout
export const NAMES_BY_BOUQUET = BOUQUETS_INDEX_HELPER()

function BOUQUETS_INDEX_HELPER() {
  const map = {}
  NAMES.forEach((n) => {
    if (!map[n.bouquet]) map[n.bouquet] = []
    map[n.bouquet].push(n)
  })
  Object.values(map).forEach((arr) => arr.sort((a, b) => a.order - b.order))
  return map
}

export function findName(id) {
  return NAMES.find((n) => n.id === id)
}

